DROP DATABASE IF EXISTS renewphonedb;
CREATE DATABASE renewphonedb;
USE renewphonedb;

DROP TABLE IF EXISTS products;
DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS buys;

CREATE TABLE products (
  id int primary key AUTO_INCREMENT,
  name varchar(150) not null,
  ram int not null,
  display_size float not null,
  storage int not null,
  price int default 0,
  quantity int default 0,
  color varchar(20),
  brand varchar(25),
  year int not null,
  category varchar(15),
  state char(12)
);

CREATE TABLE users (
  id int primary key AUTO_INCREMENT,
  name varchar(25) not null,
  surname varchar(25) not null,
  email varchar(100) not null unique,
  password varchar(50) not null,
  address varchar(60) not null,
  city varchar(35) not null,
  cap char(7) not null,
  phone varchar(20) not null
);

CREATE TABLE orders (
  id int primary key AUTO_INCREMENT,
  id_user int not null,
  total int not null,
  order_date date not null,
  FOREIGN KEY (id_user) REFERENCES users(id)
);

CREATE TABLE order_items (
  id int primary key AUTO_INCREMENT,
  id_order int ,
  id_product int,
  ordered_quantity int not null,
  
  /* Servono per mantenere i dati del prodotto nell'ordine */
  name varchar(150) not null,
  ram int not null,
  display_size float not null,
  storage int not null,
  price int default 0,
  color varchar(20),
  brand varchar(25),
  year int not null,
  category varchar(15),
  state char(12),
  
  FOREIGN KEY (id_order) REFERENCES orders(id) on delete set null,
  FOREIGN KEY (id_product) REFERENCES products(id) on delete set null
);

CREATE TABLE carts (
  id_user int not null,
  id_product int not null,
  quantity int not null,
  primary key (id_user, id_product),
  FOREIGN KEY (id_user) REFERENCES users(id) on delete cascade,
  FOREIGN KEY (id_product) REFERENCES products(id)
);


